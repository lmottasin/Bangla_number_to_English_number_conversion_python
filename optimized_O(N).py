bangla_number = input("Enter a Bangla number/ একটি বাংলা নাম্বার দিন : ")

# Testing the function
# ফাংশনটি কাজ করছে কিনা দেখি

print("English Number/ ইংলিশ নাম্বারটি :", bangla_to_eng_num_covert(bangla_number))